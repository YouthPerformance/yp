/**
 * Get a specific day by number (1-indexed)
 */
export function getDay(dayNumber: number): WorkoutDay | undefined {
  return DAYS.find((d) => d.dayNumber === dayNumber);
}

/**
 * Get next day after a given day
 */
export function getNextDay(currentDay: number): WorkoutDay | undefined {
  return DAYS.find((d) => d.dayNumber === currentDay + 1);
}
